//Java Program to print the sum of first 100 even numbers using a for loop 
public class SumOfEvenNumbers {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 100; i++) {
            sum += i * 2;
        }
        System.out.println("The sum of first 100 even numbers is: " + sum);
    }
}






